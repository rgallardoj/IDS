select 	concat(nombre,' ',apellido_paterno,' ',apellido_materno) as nombre,
	concat(direccion,' #',numcasa) as direccion,
        perfil.descripcion as perfil,
        comunas.comuna_nombre as comuna,
        activo 

from usuario inner join perfil on 
usuario.idperfil = perfil.idperfil inner join comunas on
comunas.comuna_id = usuario.comuna_id
where rut = 17953813;

